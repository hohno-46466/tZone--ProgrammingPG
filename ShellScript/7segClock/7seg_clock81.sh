#!/bin/bash

# First version: 2026-06-30(Tue) by @hohno_at_MonkKB & Gemini AI
# Last update:   2026-07-03(Fri) (定期フルリフレッシュ機能の追加)

# ------------------------------------------------------------------------------
# 1. シグナルハンドリング基本設定
# ------------------------------------------------------------------------------
# 終了時（Ctrl+C または kill）はカーソルを戻し、画面をクリアして正常終了コード(130)を返す
trap 'printf "\e[?25h"; clear; exit 130' INT TERM

# ------------------------------------------------------------------------------
# 2. 画面初期化と7セグ用アスキーアート定義
# ------------------------------------------------------------------------------
clear
printf "\e[?25l"	# カーソル非表示

# すべての文字を「完全に6マス（5マスAA＋隙間1マス）」の等幅に固定
declare -a N0=(" ###  " "#   # " "#   # " "#   # " " ###  ")
declare -a N1=("  #   " " ##   " "  #   " "  #   " " ###  ")
declare -a N2=(" ###  " "    # " " ###  " "#     " " ###  ")
declare -a N3=(" ###  " "    # " " ###  " "    # " " ###  ")
declare -a N4=(" #  # " "#   # " " #####" "    # " "    # ")
declare -a N5=(" #####" "#     " " #### " "    # " "####  ")
declare -a N6=(" ###  " "#     " " #### " "#   # " " ###  ")
declare -a N7=(" #####" "    # " "   #  " "  #   " "  #   ")
declare -a N8=(" ###  " "#   # " " ###  " "#   # " " ###  ")
declare -a N9=(" ###  " "#   # " " #### " "    # " " ###  ")
declare -a N_COLON=("      " "  #   " "      " "  #   " "      ")

get_line() {
    local char=$1
    local line_idx=$2
    if [ "$char" = ":" ]; then
        echo "${N_COLON[$line_idx]}"
    else
        local var_name="N$char[$line_idx]"
        echo "${!var_name}"
    fi
}

# ------------------------------------------------------------------------------
# 3. 定期クリア用の設定
# ------------------------------------------------------------------------------
LOOP_COUNTER=0

# 他のシステムサイクル（10秒、60秒等）との「共振・うなり」を避けるため素数を採用。
# 0.1秒×131回 ＝ 約13.1秒ごとに自動で画面全体を強制リフレッシュします。
REFRESH_CYCLE=131

# 【利用上の注意】
# Ctrl+Zで一度バックグラウンドに回し(suspend)、他の作業をしてから `fg` で復帰
# させた場合など、直前の作業画面の残骸で時計の表示が乱れることがあります。
# システム上、最長で「15秒」ほどそのままお待ちいただければ、上記の定期リフレッシュ
# が自動で発動し、何事もなかったかのように綺麗な中央表示に自己修復されます。
# ------------------------------------------------------------------------------

# ------------------------------------------------------------------------------
# 4. メイン描画ループ
# ------------------------------------------------------------------------------
while true; do
    # --------------------------------------------------------------------------
    # 防御セクション(1): 画面サイズ取得とシグナルブロック
    # --------------------------------------------------------------------------
    # これより下の計算・描画処理の「一瞬」はSIGWINCHを一時的に無視。
    # コマンド実行中のシグナル直撃を防ぎ、Bashがwhileループを脱出するバグを根絶する。
    trap '' SIGWINCH

    # ターミナルのサイズ取得（失敗時は固有のエラーコードで安全に落とす）
    LINES=$(tput lines) || { printf "\e[?25h\n[ERROR] tput lines failed\n"; exit 91; }
    COLS=$(tput cols)   || { printf "\e[?25h\n[ERROR] tput cols failed\n";  exit 92; }

    # --------------------------------------------------------------------------
    # 【追加】カウンターによる定期フルクリア処理
    # --------------------------------------------------------------------------
    # Ctrl+Zからの復帰や、予期せぬ画面の乱れ（ゴースト）を一定時間ごとに強制リセットする安全弁
    LOOP_COUNTER=$((LOOP_COUNTER + 1))
    if [ $LOOP_COUNTER -ge $REFRESH_CYCLE ]; then
        clear
        LOOP_COUNTER=0
    fi

    # --------------------------------------------------------------------------
    # 防御セクション(2): 時間データの取得と描画計算
    # --------------------------------------------------------------------------
    DATE_STR=$(LC_TIME=C date "+%Y-%m-%d (%a)") || continue
    HMS_STR=$(date "+%H:%M:%S")                 || continue

    # 全体の幅：6マス × 8文字 = 48マス
    TOTAL_HEIGHT=7 
    TOTAL_WIDTH=48  

    START_ROW=$(((LINES - TOTAL_HEIGHT) / 2))
    START_COL=$(((COLS - TOTAL_WIDTH) / 2))

    # 1. 年月日の描画
    DATE_COL=$(( START_COL + (TOTAL_WIDTH - ${#DATE_STR}) / 2 ))
    printf "\e[%d;%dH\e[K%s" "$START_ROW" "$DATE_COL" "$DATE_STR" || { printf "\e[?25h\n[ERROR] date printf failed\n"; exit 93; }

    # 2. 7セグ時分秒の描画（5行分のAAをループ結合して出力）
    for i in {0..4}; do
        LINE_BUF=""
        for (( j=0; j<${#HMS_STR}; j++ )); do
            CHAR="${HMS_STR:$j:1}"
            PART=$(get_line "$CHAR" "$i")
            LINE_BUF="${LINE_BUF}${PART}"
        done

        TARGET_ROW=$((START_ROW + 2 + i))
        printf "\e[%d;%dH\e[K%s" "$TARGET_ROW" "$START_COL" "$LINE_BUF" || { printf "\e[?25h\n[ERROR] 7seg printf failed\n"; exit 94; }
    done

    # --------------------------------------------------------------------------
    # 解放セクション: sleepの間だけサイズ変更を受け付ける
    # --------------------------------------------------------------------------
    # ループの大部分を占める「sleep」の直前でトラップを解放し、即座のclearでゴーストを防ぐ。
    # 末尾に「; true」を添えてシグナル終了時のステータスを0にロンダリング。
    # ユーザーが手動で画面サイズを変えた時は、定期クリアを待たずに即座に手動クリアを実行させる
    # その際、定期クリアのカウンターも0にリセットして無駄な連続クリアを防ぐ
    trap 'clear; LOOP_COUNTER=0; true' SIGWINCH

    # 0.1秒待機。シグナルで中断されても「|| true」で衝撃を吸収しループを維持。
    sleep 0.1 || true
done

# 万が一ループを突き抜けた場合のバグトラップ
exit 95
