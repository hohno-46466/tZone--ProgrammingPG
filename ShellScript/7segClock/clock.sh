# ==============================================================================
# ターミナル中央表示型 デジタルクロック (Mac / Linux 両対応版)
# ==============================================================================
# [特徴]
# 1. ターミナルの中央に、NTPに同期したシステムクロックをリアルタイム表示します。
# 2. sleepを用いた適度なウェイトにより、CPU負荷をほぼゼロ(busy loop回避)に抑えます。
# 3. macOS(BSD系)とLinux(Ubuntu等)の環境差（echoやdateコマンドの仕様差）を吸収します。
# 4. SIGWINCHシグナルを捕捉し、画面サイズ変更時のみ一瞬クリアして残骸を防ぎます。
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. シグナルハンドリング設定 (trap)
# ------------------------------------------------------------------------------
# 終了時（Ctrl+C または kill）に非表示にしていたカーソルを再表示し、画面を戻す
trap 'printf "\e[?25h"; clear; exit' INT TERM

# ユーザーによるウィンドウサイズ変更（SIGWINCH）を検知した瞬間のみ、画面をクリアする
# これにより、通常ループの負荷を上げずに、サイズ変更時の文字の残骸（ゴースト）を防ぐ
trap 'clear' SIGWINCH

# ------------------------------------------------------------------------------
# 2. 画面の初期化
# ------------------------------------------------------------------------------
clear
printf "\e[?25h" # カーソル状態を一度リセット
printf "\e[?25l" # カーソルを非表示にして時計を見やすくする

# ------------------------------------------------------------------------------
# 3. 環境依存の事前チェック (Mac date コマンドへの対応)
# ------------------------------------------------------------------------------
# macOS標準のdateは「%1N(ミリ秒1桁)」に対応していないため、ループ前に利用可能か判定する。
# 実際に一度叩いてエラーにならず、かつ"%1N"という文字列がそのまま返ってこないかを確認。
if date "+%1N" >/dev/null 2>&1 && [ "$(date "+%1N")" != "%1N" ]; then
    HAS_MILLISEC=true
else
    HAS_MILLISEC=false
fi

# ------------------------------------------------------------------------------
# 4. メイン描画ループ
# ------------------------------------------------------------------------------
while true; do
    # 現在のターミナルの最大行数(lines)と最大列数(cols)を動的に取得
    LINES=$(tput lines)
    COLS=$(tput cols)

    # --- 時間データの取得（レースコンディション対策） ---
    # 時分秒とミリ秒を別々のdateコマンドで取得すると、実行のわずかな時間差で
    # 秒の繰り上がり時に表示がズレる（例: 58.9秒の後に58.0秒と表示される等）バグが起きる。
    # これを防ぐため、事前チェックのフラグに基づき「必ず1回のdateコマンド」で文字列を完結させる。
    # また、LC_TIME=C を付与して環境依存（日本語環境等）を排除し、曜日を確実に半角英数(Tue等)にする。
    if [ "$HAS_MILLISEC" = true ]; then
        # Linux (Ubuntu) 環境：ミリ秒（ナノ秒1桁）まで同期して取得
        TIME_STR=$(LC_TIME=C date "+%Y-%m-%d (%a) %H:%M:%S.%1N")
    else
        # macOS 標準環境など：%1N非対応時は一貫性を保つため末尾を「.0」に固定
        TIME_STR=$(LC_TIME=C date "+%Y-%m-%d (%a) %H:%M:%S.0")
    fi

    # 描画文字列の長さを取得（すべて半角英数化されているため、Mac/Linux問わず文字数＝バイト数で安全）
    STR_LEN=${#TIME_STR}

    # 現在の画面サイズを基に、文字列がちょうど中央に配置される座標を計算
    TARGET_ROW=$((LINES / 2))
    TARGET_COL=$(((COLS - STR_LEN) / 2))

    # --- 画面への描画処理 ---
    # \e[%d;%dH : 計算した中央座標へカーソルをダイレクトに移動
    # \e[K       : 行の描画開始位置から行末までの古い残骸をクリア（画面全体のクリアより遥かに高速）
    # %s         : エスケープシーケンスの動的評価(%b)を避け、%sで純粋な文字列として安全に出力
    printf "\e[%d;%dH\e[K%s" "$TARGET_ROW" "$TARGET_COL" "$TIME_STR"

    # 0.05秒（50ミリ秒）待機。
    # busy loopによるCPU使用率100%を防止しつつ、秒の切り替わり（.0秒になる瞬間）を
    # 人間の目に滑らか（最大ラグ0.05秒以内）に追従させるための最適なウェイト。
    sleep 0.05
done
