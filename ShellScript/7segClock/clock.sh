#!/bin/bash

# Last update: 2026-07-03(Fri) 08:37 JST / 2026-07-02(Thu) 23:37 UTC by @hohno_at_monkKB with Gemini AI

# ==============================================================================
# ターミナル中央表示型 デジタルクロック (シグナル完全克服・Mac/Linux両対応版)
# ==============================================================================
# [開発記録]
# First version: 2026-06-30(Tue) by @hohno_at_MonkKB & Gemini AI
# Last update:   2026-07-03(Fri) (シグナル競合およびループ脱出バグの完全修正)
#
# [特徴・設計意図]
# 1. ターミナル中央へのリアルタイム同期表示（sleep 0.05秒による低負荷駆動）
# 2. macOS(BSD系) / Linux(Ubuntu等)の環境依存（dateのミリ秒仕様差）を動的吸収
# 3. SIGWINCH（画面サイズ変更）検知時のみのイベント駆動型 `clear`（ゴースト残像の防止）
# 4. 【最重要】計算・描画中の一時的なシグナルマスク（trap ''）と、sleep中のシグナル解放
#    により、激しい画面伸縮時にBashがwhileループを強制ブレイクして終了するバグを完全封殺。
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. シグナルハンドリング設定 (基本設定)
# ------------------------------------------------------------------------------
# 終了時（Ctrl+C または kill）は、非表示にしていたカーソルを再表示し、画面をクリアして正常終了コード(130)を返す
trap 'printf "\e[?25h"; clear; exit 130' INT TERM

# ------------------------------------------------------------------------------
# 2. 画面の初期化
# ------------------------------------------------------------------------------
clear
printf "\e[?25h" # カーソル状態を一度リセット
printf "\e[?25l" # カーソルを非表示にして時計の視認性を向上

# ------------------------------------------------------------------------------
# 3. 環境依存の事前チェック (Mac/Linux date コマンド互換性判定)
# ------------------------------------------------------------------------------
# macOS標準のdateは「%1N(ミリ秒1桁)」に対応していないため、ループ前に成否を判定しておく
if date "+%1N" >/dev/null 2>&1 && [ "$(date "+%1N")" != "%1N" ]; then
    HAS_MILLISEC=true
else
    HAS_MILLISEC=false
fi

# ------------------------------------------------------------------------------
# 4. メイン描画ループ
# ------------------------------------------------------------------------------
while true; do

    # --------------------------------------------------------------------------
    # 防御セクション①: 画面サイズ取得とシグナルブロック
    # --------------------------------------------------------------------------
    # tputによるサイズ取得および、これより下の時間計算・描画処理の「一瞬」はSIGWINCHを一時的に無視する。
    # コマンド実行中にシグナルが直撃して中断されるのを防ぎ、Bashがwhileループを脱出するバグを根絶する。
    trap '' SIGWINCH

    # 現在のターミナルの最大行数(lines)と最大列数(cols)を動的に取得
    # 万が一シグナル等で失敗した場合は、原因特定のバグトラップとして固有コードで安全に終了させる
    LINES=$(tput lines) || { printf "\e[?25h\n[ERROR] tput lines failed\n"; exit 91; }
    COLS=$(tput cols)   || { printf "\e[?25h\n[ERROR] tput cols failed\n";  exit 92; }

    # --------------------------------------------------------------------------
    # 防御セクション②: 時間データの取得（レースコンディション対策）
    # --------------------------------------------------------------------------
    # 時分秒とミリ秒を別々のdateコマンドで取得すると、実行のわずかな時間差で表示がズレるため一発で取得する
    # 万が一dateがシグナルで割れた場合は、「|| continue」でこの回のループを安全にスキップする
    # LC_TIME=C を付与して環境依存を排除し、曜日を確実に半角英数(Tue等)にする
    if [ "$HAS_MILLISEC" = true ]; then
        TIME_STR=$(LC_TIME=C date "+%Y-%m-%d (%a) %H:%M:%S.%1N") || continue
    else
        TIME_STR=$(LC_TIME=C date "+%Y-%m-%d (%a) %H:%M:%S.0") || continue
    fi
    
    # 描画文字列の長さを取得（すべて半角英数化されているため文字数＝バイト数で安全）
    STR_LEN=${#TIME_STR}

    # 現在の画面サイズを基に、文字列がちょうど中央に配置される座標を計算
    TARGET_ROW=$((LINES / 2))
    TARGET_COL=$(((COLS - STR_LEN) / 2))

    # --------------------------------------------------------------------------
    # 防御セクション③: 画面への描画処理
    # --------------------------------------------------------------------------
    # \e[%d;%dH : 計算した中央座標へカーソルを移動
    # \e[K       : 行の描画開始位置から行末までの古い残骸をクリア（全体クリアより圧倒的に高速）
    # %s         : エスケープシーケンスの動的評価(%b)を避け、純粋な文字列として安全に出力
    printf "\e[%d;%dH\e[K%s" "$TARGET_ROW" "$TARGET_COL" "$TIME_STR" || { printf "\e[?25h\n[ERROR] printf failed\n"; exit 93; }

    # --------------------------------------------------------------------------
    # 解放セクション: sleepの間だけサイズ変更を受け付ける
    # --------------------------------------------------------------------------
    # ループの時間の大部分を占める「sleep 0.05」の直前でトラップを解放する。
    # ユーザーが画面サイズを変えた瞬間に「即座にclear」が走るため、ゴースト（残像）を完璧に防ぐ。
    # 末尾に「; true'」を添えることで、シグナル終了時のステータスを0にロンダリングしシェルの誤動作を防ぐ。
    trap 'clear; true' SIGWINCH
    
    # 0.05秒待機。シグナルで中断されて非ゼロを返しても「|| true」で衝撃を吸収しループを維持する
    sleep 0.05 || true
done

# 通常は上の無限ループを絶対に突き抜けないが、万が一シェル自体の不具合等で漏れた場合はここで検知
exit 94
